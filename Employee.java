public class Employee {
    String name;
    int hourlyWage;
    int workHours;
    int hireYear;


    Employee(String name, int hourlyWage, int workHours, int hireYear){
        this.name = name;
        this.hourlyWage = hourlyWage;
        this.workHours = workHours;
        this.hireYear = hireYear;
    }


}
